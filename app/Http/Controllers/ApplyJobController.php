    {
        $user_id = User::findOrfail($userid);
        $job_list = Jobs::findorfail($jobid);
        $applyjob= ApplyJob::create([
            'user_id' => $user_id->id,
            'job_id' => $job_list->id,
        ]);
        if($applyjob){
            return response()->json([
                'status' => 'success',
            ]);
        }
    }

